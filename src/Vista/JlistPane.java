    private DefaultListModel modeloLista()
    {
        
        DefaultListModel<String> modelo = new DefaultListModel<>();
        
        Node aux = principal.lista.getFirstNode();
        
        while(aux != null)
        {
            if (aux.getMain() instanceof Empleado)
            {
                    
                String infoObjeto = "Informaci√≥n del objeto: " + aux.getMain().toString();
                modelo.addElement(infoObjeto);  

            }    
            else
            {
                modelo.addElement("No hay elementos");
            }
            aux = aux.getNextNode();
        
        }
        return modelo;
    }

